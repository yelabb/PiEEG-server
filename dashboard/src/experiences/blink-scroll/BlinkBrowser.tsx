// Browse Mode
// ═════════════════════════════════════════════════════════════════════════════

interface BrowseModeProps {
  eegData: EEGData;
  calibration: BlinkCalibration;
  onRecalibrate: () => void;
  onExit: () => void;
}

function BrowseMode({
  eegData,
  calibration,
  onRecalibrate,
  onExit,
}: BrowseModeProps) {
  const [scrollDir, setScrollDir] = useState<"down" | "up">("down");
  const [blinkFlash, setBlinkFlash] = useState(false);
  const [enabled, setEnabled] = useState(true);
  const [scrollAmount, setScrollAmount] = useState(calibration.scrollAmount);
  const [amp, setAmp] = useState(0);
  const [blinkTotal, setBlinkTotal] = useState(0);
  const [mode, setMode] = useState<"reader" | "web">("reader");
  const [url, setUrl] = useState(
    "https://en.wikipedia.org/wiki/Electroencephalography",
  );
  const [iframeUrl, setIframeUrl] = useState("");
  const [iframeScroll, setIframeScroll] = useState(0);
  const [showSettings, setShowSettings] = useState(false);
  const [threshold, setThreshold] = useState(calibration.threshold);

  const contentRef = useRef<HTMLDivElement>(null);
  const stateRef = useRef<"idle" | "in-blink">("idle");
  const lastBlinkRef = useRef(0);
  const blinkStartRef = useRef(0);
  const flashTimeoutRef = useRef(0);
  const ampRef = useRef(0);
  const lastAmpUpdate = useRef(0);
  const doScrollRef = useRef<() => void>(() => {});

  // ── Scroll action ──
  const doScroll = useCallback(() => {
    setBlinkTotal((c) => c + 1);
    setBlinkFlash(true);
    clearTimeout(flashTimeoutRef.current);
    flashTimeoutRef.current = window.setTimeout(
      () => setBlinkFlash(false),
      200,
    );

    const amount = scrollDir === "down" ? scrollAmount : -scrollAmount;

    if (mode === "reader" && contentRef.current) {
      contentRef.current.scrollBy({ top: amount, behavior: "smooth" });
    } else if (mode === "web") {
      setIframeScroll((prev) => Math.max(0, prev + amount));
    }
  }, [scrollDir, scrollAmount, mode]);

  // Keep doScrollRef in sync so the RAF loop always calls the latest version
  doScrollRef.current = doScroll;

  // ── Blink detection loop (RAF) ──
  useEffect(() => {
    if (!enabled) return;

    const WINDOW = 25; // 100ms
    const REFRACTORY_MS = 400;
    const MAX_BLINK_MS = 600;
    const MIN_BLINK_MS = 30;
    const CHECK_MS = 20;

    let lastCheck = 0;
    let raf: number;

    function check() {
      const now = performance.now();
      if (now - lastCheck < CHECK_MS) {
        raf = requestAnimationFrame(check);
        return;
      }
      lastCheck = now;

      const a = readAmplitude(eegData, calibration.channels, WINDOW);
      ampRef.current = a;

      // Throttle React state update for amplitude display (every 80ms)
      if (now - lastAmpUpdate.current > 80) {
        lastAmpUpdate.current = now;
        setAmp(a);
      }

      const above = a > threshold;
      const state = stateRef.current;

      if (state === "idle") {
        if (above && now - lastBlinkRef.current > REFRACTORY_MS) {
          stateRef.current = "in-blink";
          blinkStartRef.current = now;
        }
      } else if (state === "in-blink") {
        if (!above) {
          const dur = now - blinkStartRef.current;
          if (dur >= MIN_BLINK_MS && dur <= MAX_BLINK_MS) {
            lastBlinkRef.current = now;
            stateRef.current = "idle";
            doScrollRef.current();
          } else {
            stateRef.current = "idle";
          }
        } else if (now - blinkStartRef.current > MAX_BLINK_MS) {
          stateRef.current = "idle";
        }
      }

      raf = requestAnimationFrame(check);
    }

    raf = requestAnimationFrame(check);
    return () => cancelAnimationFrame(raf);
  }, [enabled, eegData, calibration, threshold]);

  // ── Keyboard shortcuts ──
  useEffect(() => {
    const handler = (e: KeyboardEvent) => {
      if (e.target instanceof HTMLInputElement) return;
      switch (e.key) {
        case "Escape":
          if (showSettings) setShowSettings(false);
          else onExit();
          break;
        case " ":
          e.preventDefault();
          setScrollDir((d) => (d === "down" ? "up" : "down"));
          break;
        case "s":
        case "S":
          setShowSettings((v) => !v);
          break;
        case "+":
        case "=":
          setScrollAmount((v) => Math.min(800, v + 50));
          break;
        case "-":
        case "_":
          setScrollAmount((v) => Math.max(50, v - 50));
          break;
      }
    };
    window.addEventListener("keydown", handler);
    return () => window.removeEventListener("keydown", handler);
  }, [onExit, showSettings]);

  // ── Amplitude meter ──
  const ampPct = Math.min(100, (amp / (threshold * 2)) * 100);
  const threshPct = Math.min(100, (threshold / (threshold * 2)) * 100);
  const meterColor = amp > threshold ? "#00c853" : "#333";

  return (
    <div style={S.root}>
      {/* ── Header ── */}
      <div style={S.header}>
        <button style={S.btnSmall} onClick={onExit}>
          ← Exit
        </button>
        <div style={S.headerTitle}>Blink Browser</div>

        {/* Amplitude meter */}
        <div style={S.meterWrap}>
          <div
            style={{
              ...S.meterFill,
              width: `${ampPct}%`,
              background: meterColor,
            }}
          />
          <div style={{ ...S.meterThreshLine, left: `${threshPct}%` }} />
        </div>

        {/* Blink indicator */}
        <div
          style={{
            ...S.blinkBadge,
            background: blinkFlash
              ? "rgba(0,200,83,0.2)"
              : "rgba(255,255,255,0.04)",
            color: blinkFlash ? "#00c853" : "#666",
            border: blinkFlash
              ? "1px solid rgba(0,200,83,0.3)"
              : "1px solid rgba(255,255,255,0.06)",
          }}
        >
          {blinkFlash ? "BLINK" : `${blinkTotal} blinks`}
        </div>

        {/* Direction toggle */}
        <button
          style={S.dirBtn}
          onClick={() =>
            setScrollDir((d) => (d === "down" ? "up" : "down"))
          }
          title="Toggle scroll direction (Space)"
        >
          {scrollDir === "down" ? "↓" : "↑"} {scrollDir}
        </button>

        {/* Enable toggle */}
        <button
          style={{
            ...S.btnSmall,
            background: enabled ? "rgba(0,200,83,0.1)" : "#111",
            color: enabled ? "#00c853" : "#666",
            borderColor: enabled
              ? "rgba(0,200,83,0.2)"
              : "rgba(255,255,255,0.1)",
          }}
          onClick={() => setEnabled((v) => !v)}
        >
          {enabled ? "● ON" : "○ OFF"}
        </button>

        {/* Settings */}
        <button
          style={S.btnSmall}
          onClick={() => setShowSettings((v) => !v)}
          title="Settings (S)"
        >
          ⚙
        </button>
      </div>

      {/* ── URL bar / mode tabs ── */}
      <div style={S.urlBar}>
        <button
          style={{
            ...S.modeTab,
            background:
              mode === "reader" ? "rgba(0,112,243,0.1)" : "transparent",
            color: mode === "reader" ? "#3291ff" : "#666",
            borderColor:
              mode === "reader"
                ? "rgba(0,112,243,0.2)"
                : "rgba(255,255,255,0.08)",
          }}
          onClick={() => setMode("reader")}
        >
          📖 Reader
        </button>
        <button
          style={{
            ...S.modeTab,
            background:
              mode === "web" ? "rgba(0,112,243,0.1)" : "transparent",
            color: mode === "web" ? "#3291ff" : "#666",
            borderColor:
              mode === "web"
                ? "rgba(0,112,243,0.2)"
                : "rgba(255,255,255,0.08)",
          }}
          onClick={() => setMode("web")}
        >
          🌐 Web
        </button>

        {mode === "web" && (
          <>
            <input
              style={S.urlInput}
              value={url}
              onChange={(e) => setUrl(e.target.value)}
              onKeyDown={(e) => {
                if (e.key === "Enter") {
                  let u = url.trim();
                  if (u && !u.startsWith("http")) u = "https://" + u;
                  setIframeUrl(u);
                  setIframeScroll(0);
                }
              }}
              placeholder="Enter URL and press Enter…"
              spellCheck={false}
            />
            <button
              style={S.btnSmall}
              onClick={() => {
                let u = url.trim();
                if (u && !u.startsWith("http")) u = "https://" + u;
                setIframeUrl(u);
                setIframeScroll(0);
              }}
            >
              Go
            </button>
          </>
        )}
      </div>

      {/* ── Content area ── */}
      {mode === "reader" ? (
        <div ref={contentRef} style={S.content}>
          {ARTICLES.map((art, i) => (
            <div key={i}>
              {i > 0 && <hr style={S.articleDivider} />}
              <article style={S.article}>
                <h1 style={S.articleTitle}>{art.title}</h1>
                <div style={S.articleBody}>{art.body}</div>
              </article>
            </div>
          ))}
          <div
            style={{
              textAlign: "center",
              padding: "60px 0",
              color: "#444",
              fontSize: 14,
            }}
          >
            — End of articles —
          </div>
        </div>
      ) : (
        <div style={S.iframeWrap}>
          {iframeUrl ? (
            <iframe
              src={iframeUrl}
              title="Blink Browser"
              sandbox="allow-scripts allow-same-origin allow-forms"
              referrerPolicy="no-referrer"
              style={{
                ...S.iframe,
                transform: `translateY(${-iframeScroll}px)`,
              }}
            />
          ) : (
            <div
              style={{
                flex: 1,
                display: "flex",
                alignItems: "center",
                justifyContent: "center",
                color: "#444",
                fontSize: 14,
              }}
            >
              Enter a URL above and press Enter or Go.
              <br />
              Note: some websites block iframe embedding.
            </div>
          )}
        </div>
      )}

      {/* ── Settings panel ── */}
      {showSettings && (
        <div style={S.settingsPanel}>
          <div>
            <div style={S.settLabel}>Scroll amount</div>
            <div style={{ display: "flex", alignItems: "center", gap: 8 }}>
              <button
                style={S.btnSmall}
                onClick={() => setScrollAmount((v) => Math.max(50, v - 50))}
              >
                −
              </button>
              <span style={S.settValue}>{scrollAmount}px</span>
              <button
                style={S.btnSmall}
                onClick={() => setScrollAmount((v) => Math.min(800, v + 50))}
              >
                +
              </button>
            </div>
          </div>
          <div>
            <div style={S.settLabel}>Threshold</div>
            <div style={{ display: "flex", alignItems: "center", gap: 8 }}>
              <button
                style={S.btnSmall}
                onClick={() => setThreshold((v) => Math.max(20, v - 10))}
              >
                −
              </button>
              <span style={S.settValue}>{threshold.toFixed(0)} µV</span>
              <button
                style={S.btnSmall}
                onClick={() => setThreshold((v) => Math.min(500, v + 10))}
              >
                +
              </button>
            </div>
          </div>
          <div>
            <div style={S.settLabel}>Channels</div>
            <span style={S.settValue}>
              {calibration.channels.map((c) => `Ch${c}`).join(", ")}
            </span>
          </div>
          <div>
            <div style={S.settLabel}>Calibrated</div>
            <span style={{ ...S.settValue, fontSize: 12 }}>
              {new Date(calibration.timestamp).toLocaleString()}
            </span>
          </div>
          <button style={S.btnSmall} onClick={onRecalibrate}>
            🔄 Recalibrate
          </button>
          <div style={{ flex: 1 }} />
          <button
            style={S.btnSmall}
            onClick={() => setShowSettings(false)}
          >
            Close (S)
          </button>
        </div>
      )}

      {/* ── Footer ── */}
      <div style={S.footer}>
        <span>
          <strong>Space</strong> toggle direction
        </span>
        <span>
          <strong>+/−</strong> scroll amount
        </span>
        <span>
          <strong>S</strong> settings
        </span>
        <span>
          <strong>Esc</strong> exit
        </span>
        <div style={{ flex: 1 }} />
        <span style={{ fontFamily: "'Geist Mono',monospace" }}>
          {amp.toFixed(0)} µV
        </span>
      </div>
    </div>
  );
}

