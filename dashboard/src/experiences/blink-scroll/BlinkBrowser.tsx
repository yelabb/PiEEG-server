interface Props {
  eegData: EEGData;
  yScale: number;
  onExit: () => void;
}

export default function BlinkBrowser({ eegData, yScale, onExit }: Props) {
  const [calibration, setCalibration] = useState<BlinkCalibration | null>(
    () => loadCalibration(),
  );
  const [showCal, setShowCal] = useState(!loadCalibration());

  if (showCal) {
    return (
      <CalibrationWizard
        eegData={eegData}
        existing={calibration}
        onComplete={(cal) => {
          saveCalibration(cal);
          setCalibration(cal);
          setShowCal(false);
        }}
        onSkip={calibration ? () => setShowCal(false) : undefined}
        onExit={onExit}
      />
    );
  }

  return (
    <BrowseMode
      eegData={eegData}
      calibration={calibration!}
      onRecalibrate={() => setShowCal(true)}
      onExit={onExit}
    />
  );
}
